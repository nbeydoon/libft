#include "libft.h"

char				*ft_strcpy(char *dst, const char *src)
{
	size_t			n;

	n = ft_strlen(src);
	if (src != NULL && dst != NULL)
		ft_memcpy(dst, src, (n + 1));
	return (dst);
}
